/**
 * The class create 4 method of arithmetic type
 * @author Gaetano Trefiletti
 */
public class ArithmeticOperators {
    /**
     * The first method is a sum of the params
     * @param num1 this is the first param int
     * @param num2 this is the secondo param int
     * @return the method return the sum of the params
     */
    public int sum(int num1,int num2){
        return num1+num2;
    }

    /**
     * The second method is a substraction of the params
     * @param num1 this is the first param int
     * @param num2 this is the second param int
     * @return the method return the substraction of the params
     */
    public int sub(int num1,int num2){
        return num1-num2;
    }

    /**
     * The third method is a multiplication of the params
     * @param num1 this is the first param int
     * @param num2 this is the second param int
     * @return the method return the multiplication of the params
     */
    public int mul(int num1,int num2){
        return num1*num2;
    }

    /**
     * The fourth method is a division of the params
     * @param num1 this is the first param int
     * @param num2 this is the second param int
     * @return the method return the division of the params
     */
    public int div(int num1,int num2){
        return num1/num2;
    }
}
